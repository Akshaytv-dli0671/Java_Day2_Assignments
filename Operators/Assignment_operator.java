package Operators;

public class Assignment_operator {
    public static void main(String[] args) {
        int a=10;
        a+=2;
        System.out.println(a);
        a-=2;
        System.out.println(a);
        a*=2;
        System.out.println(a);
        a/=2;
        System.out.println(a);
        a%=2;
        System.out.println(a);


    }
}
